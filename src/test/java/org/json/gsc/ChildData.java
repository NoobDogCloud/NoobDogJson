package org.json.gsc;

public class ChildData {
    private double d;
    private float f;

    public ChildData() {
        d = 1.d;
        f = 2.f;
    }

    public double getD() {
        return d;
    }

    public void setD(double d) {
        this.d = d;
    }

    public float getF() {
        return f;
    }

    public void setF(float f) {
        this.f = f;
    }
}
